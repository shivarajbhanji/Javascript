// Arithmetic operators
// + - * /
const now = 2024;
const ageJonas = now - 1996;
const ageSarah = now - 2000;
// exponentiation operator
// **
console.log(2 ** 3);

// + or concatenation operator
const firstName = "Rohit";
const lastName = "Sharma";
console.log(firstName + ' ' + lastName);

// Assignment operator 
// = += -= *= /= ++
let x = 10 + 5;//15
x += 10;//x=x+10=15+10=25
x++;
x--;
console.log(x);

// Comparison operator
// < > <= >=
console.log(ageJonas > ageSarah);
console.log(ageSarah >= 18)